window.currentLang = localStorage.getItem('mutatit_lang') || 'id';

function switchLanguage(lang) {
  window.currentLang = lang;
  localStorage.setItem('mutatit_lang', lang);

  // Apply translations
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      // For scramble/hover elements, we need to update the base text
      if (el.dataset.origTextBase !== undefined) {
        el.dataset.origTextBase = translations[lang][key];
        el.innerText = translations[lang][key];
      } else {
        el.innerHTML = translations[lang][key];
      }
    }
  });

  // Update toggle UI
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update HTML lang attribute
  document.documentElement.setAttribute('lang', lang);

  // Dispatch event for script.js to re-init animations
  document.dispatchEvent(new CustomEvent('langChanged'));
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
  switchLanguage(window.currentLang);
});
